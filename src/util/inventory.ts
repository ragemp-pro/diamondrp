export const getItemName = (item:InventoryItemCefObject) => {
  let name = getItemNameById(item.item_id);
  let label = ``;
  if([inventoryTypesUtil.BagTransfer, inventoryTypesUtil.Bag, inventoryTypesUtil.BagFull, inventoryTypesUtil.BagSmall].includes(item.item_id)){
    name += ' #'+item.id
  }
  if (isAmmoBox(item.item_id)){
    name += ` (${item.count} шт.)`;
  }
  if ([142, 143, 144, 145].includes(item.item_id) || (item.item_id >= 154 && item.item_id <= 180)){
    name += ` (${item.count} г.)`;
  }
  if ([275, 276].includes(item.item_id)){
    name += ` (${item.count} шт.)`;
  }
  // if ([27, 28, 29, 30].includes(item.item_id) || (item.item_id >= 146 && item.item_id <= 153)){
  //   name += ` (${item.count} шт.)`;
  // }
  if(item.item_id == 277){
    name += `На $${item.prefix} №${item.id}`;
    if (item.key == -1) name += ' (Использован)';
    else if (item.key == 0) name += ' (Ожидает розыгрыша)';
    else if (item.key == 1) name += ' (ПОБЕДА!)';
  }
  if (item.prefix > 0 && item.number > 0 && item.key <= 0) {
    label = item.prefix + "-" + item.number;
  } else if (item.key > 0) {

      if (item.item_id >= 265 && item.item_id <= 268) {

          if (item.prefix == 1)
          name = <string>clothFutil[item.key][9];
          else
          name = <string>clothMutil[item.key][9];
      }
      else if (item.item_id >= 269 && item.item_id <= 273) {
          if (item.prefix == 1)
          name = <string>propFutil[item.key][5];
          else
          name = <string>propMutil[item.key][5];
      }
      else {
          label = "#" + item.key;
      }
  }
  return name+" "+label;
}
