export const ammoItemIdToMaxCountUtil = (type: number) => {
  switch (type) {
    case 147:
      return 10;
    case 148: //... / Firework
      return 1;
    case 149: //... / RPG
      return 1;
    case 150: //... / Railgun
      return 10;
    case -152: //... / HomingLauncher
      return 1;
    case 151: //... / CompactGrenadeLauncher
      return 10;
    case 146: //12.7mm / SniperGun
      return 60;
    case 28: //18.5mm
      return 120;
    case 30: //5.56mm
      return 260;
    case 27: //9mm
      return 140;
    case 153: //9mm
      return 140;
    case 29: //7.62mm
      return 130;
    default:
      return 1;
  }
}


function range(start:number, end:number) {
  let ids:number[] = [];
  for(let id = start; id<(end+1);id++)ids.push(id)
  return ids
}

